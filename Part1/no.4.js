// What is the typeof operator used for? Provide two examples
// typeof operator in JavaScript is used to determine the data type of a value or variable.
//  It returns a string indicating the type, such as “string”, “number”, “boolean”, “object”
// example:
console.log(typeof "Hello");
console.log(typeof 42);     
console.log(typeof true);
console.log(typeof {});      
console.log(typeof undefined); 
